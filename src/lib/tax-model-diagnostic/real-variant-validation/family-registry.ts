type InventorySeed = Omit<
  CorpusInventoryRow,
  | "officialSyntheticCount"
  | "realAnonymizedCount"
  | "holdoutCount"
  | "coverageStatus"
  | "gapPriority"
  | "lastVerifiedAt"
>;

function row(seed: InventorySeed): CorpusInventoryRow {
  return Object.freeze({
    ...seed,
    fiscalYears: Object.freeze([...seed.fiscalYears]),
    layoutIds: Object.freeze([...seed.layoutIds]),
    officialSyntheticCount: 0,
    realAnonymizedCount: 0,
    holdoutCount: 0,
    coverageStatus: "REAL_VARIANT_GAP" as const,
    gapPriority: HIGH_PRIORITY.has(seed.family as RealVariantFamily)
      ? ("HIGH" as const)
      : ("NORMAL" as const),
    lastVerifiedAt: "2026-07-15",
  });
}

