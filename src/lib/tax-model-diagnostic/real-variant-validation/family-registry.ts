export function isRealVariantFamily(
  value: unknown,
): value is RealVariantFamily {
  return (
    typeof value === "string" &&
    (REAL_VARIANT_FAMILIES as readonly string[]).includes(value)
  );
}

export function readCorpusInventory(): readonly CorpusInventoryRow[] {
  return CURRENT_CORPUS_INVENTORY.map((entry) => ({
    ...entry,
    fiscalYears: [...entry.fiscalYears],
    layoutIds: [...entry.layoutIds],
  }));
}
