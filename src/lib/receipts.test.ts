  it("mantiene un recibo legacy sin origen congelado bloqueado y no lo resella", () => {
    const source = paidSource();
    const currentReceipt = issuedReceiptFor(source, "receipt-legacy");
    const snapshotWithoutOrigin = {
      ...currentReceipt.documentSnapshot!,
    } as Record<string, unknown>;
    delete snapshotWithoutOrigin.sourceDocumentId;
    const legacySnapshot = asLegacyDocumentSnapshot(
      snapshotWithoutOrigin as unknown as DocumentSnapshot,
    );
    const legacyPdf = asLegacyPdfSnapshot(
      buildDocumentPdfSnapshot(legacySnapshot, PROFILE, NOW),
      legacySnapshot.snapshotHash,
    );
    const legacyReceipt: Document = {
      ...currentReceipt,
      documentSnapshot: legacySnapshot,
      pdfSnapshot: legacyPdf,
      snapshotSeal: buildDocumentSnapshotSeal(
        currentReceipt.id,
        legacySnapshot,
        legacyPdf,
      ),
    };
    const linkedSource: Document = {
      ...source,
      receiptDocumentId: legacyReceipt.id,
    };
    const originalSnapshot = legacyReceipt.documentSnapshot;
    const originalSeal = legacyReceipt.snapshotSeal;
    const data = appDataWithDocuments([linkedSource, legacyReceipt]);

    expect(
      Object.prototype.hasOwnProperty.call(
        legacyReceipt.documentSnapshot!,
        "sourceDocumentId",
      ),
    ).toBe(false);
    expect(
      inspectReceiptGeneration(data.documents, linkedSource.id),
    ).toMatchObject({
      status: "existing",
      receipt: legacyReceipt,
      integrityBlocked: true,
    });
    const checkedLegacyReceipt = withDocumentRelationshipIntegritySignals(
      data.documents,
    ).find((document) => document.id === legacyReceipt.id)!;
    expect(checkedLegacyReceipt.snapshotIntegrity?.status).toBe("blocked");
    expect(documentAmounts(checkedLegacyReceipt, false).total).toBe(0);
    const normalizedForDisplay = withDocumentRelationshipIntegritySignals(
      data.documents,
    );
    expect(
      inspectReceiptGenerationForDisplay(
        normalizedForDisplay,
        linkedSource.id,
      ),
    ).toMatchObject({
      status: "existing",
      receipt: { id: legacyReceipt.id },
      integrityBlocked: true,
    });
    expect(
      prepareReceiptGeneration(data, linkedSource.id, {
        now: NOW,
        createId: idSequence("must-not-be-used"),
      }),
    ).toMatchObject({
      status: "existing",
      receipt: legacyReceipt,
      integrityBlocked: true,
    });
    expect(legacyReceipt.documentSnapshot).toBe(originalSnapshot);
    expect(legacyReceipt.snapshotSeal).toBe(originalSeal);
    expect(data.documents).toEqual([linkedSource, legacyReceipt]);
  });

