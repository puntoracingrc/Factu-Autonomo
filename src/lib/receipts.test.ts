describe("unmarkInvoiceCollection", () => {
  function paidInvoiceWithReceipt(receipt: Document): Document {
    return markDocumentPaid(
      { ...invoice, receiptDocumentId: receipt.id },
      "2026-06-24T10:00:00.000Z",
    );
  }

  function draftReceipt(overrides: Partial<Document> = {}): Document {
    return {
      ...buildReceiptFromInvoice(invoice, PROFILE),
      id: "receipt-draft",
      number: "R-2026-0001",
      status: "borrador",
      createdAt: "2026-06-24T09:00:00.000Z",
      updatedAt: "2026-06-24T09:00:00.000Z",
      ...overrides,
    };
  }

  function emittedReceipt(overrides: Partial<Document> = {}): Document {
    return markDocumentPaid(
      issueDocument(
        draftReceipt({
          id: "receipt-issued",
          number: "R-2026-0001",
          ...overrides,
        }),
        PROFILE,
        "2026-06-24T10:00:00.000Z",
      ),
      "2026-06-24T10:05:00.000Z",
    );
  }

  it("encuentra un recibo sellado aunque se borren los vínculos vivos", () => {
    const receipt = emittedReceipt();
    const frozenOnlyReceipt: Document = {
      ...receipt,
      sourceDocumentId: undefined,
    };

    expect(
      findReceiptForInvoice([frozenOnlyReceipt], invoice.id),
    ).toBe(frozenOnlyReceipt);
  });

  it("no muta una factura cuya integridad está bloqueada", () => {
    const paid = markDocumentPaid(invoice, "2026-06-24T10:00:00.000Z");
    const corrupt: Document = {
      ...paid,
      documentSnapshot: {
        ...paid.documentSnapshot!,
        number: "F-ALTERADA",
      },
    };
    const documents = [corrupt];

    const result = unmarkInvoiceCollection(
      documents,
      corrupt.id,
      "enviado",
      "2026-06-24T11:00:00.000Z",
    );

    expect(result.documents).toBe(documents);
    expect(result.documents[0].status).toBe("pagado");
  });

  it("no muta una factura bloqueada por su relación documental", () => {
    const blocked: Document = {
      ...markDocumentPaid(invoice, "2026-06-24T10:00:00.000Z"),
      snapshotIntegrity: {
        status: "blocked",
        issues: ["document_relationship_invalid"],
      },
    };
    const documents = [blocked];

    const result = unmarkInvoiceCollection(
      documents,
      blocked.id,
      "enviado",
      "2026-06-24T11:00:00.000Z",
    );

    expect(result.documents).toBe(documents);
    expect(result.documents[0]).toBe(blocked);
  });

  it("valida el grafo y no muta una identidad fiscal duplicada", () => {
    const paid = markDocumentPaid(invoice, "2026-06-24T10:00:00.000Z");
    const duplicateIdentity = markDocumentPaid(
      issueDocument(
        {
          id: "duplicate-fiscal-identity",
          type: "factura",
          number: invoice.number,
          date: invoice.date,
          client: { ...invoice.client },
          items: invoice.items.map((item) => ({
            ...item,
            id: `duplicate-${item.id}`,
          })),
          status: "borrador",
          createdAt: "2026-06-24T09:00:00.000Z",
          updatedAt: "2026-06-24T09:00:00.000Z",
        },
        PROFILE,
        "2026-06-24T09:00:00.000Z",
      ),
      "2026-06-24T10:00:00.000Z",
    );
    const documents = [paid, duplicateIdentity];

    const result = unmarkInvoiceCollection(
      documents,
      paid.id,
      "enviado",
      "2026-06-24T11:00:00.000Z",
    );

    expect(result.documents).toBe(documents);
    expect(result.documents[0].status).toBe("pagado");
  });

  it("rechaza una colisión fiscal entre factura sellada y copia legacy", () => {
    const paid = markDocumentPaid(invoice, "2026-06-24T10:00:00.000Z");
    const legacyDuplicate: Document = {
      ...paid,
      id: "legacy-duplicate",
      pdfSnapshot: undefined,
      snapshotSeal: undefined,
      snapshotIntegrityRequired: undefined,
    };

    expect(
      findUniqueReceiptSourceInvoice(
        [paid, legacyDuplicate],
        paid.id,
      ),
    ).toBeUndefined();
  });

  it("exige que el ID de factura aparezca exactamente una vez", () => {
    const paid = markDocumentPaid(invoice, "2026-06-24T10:00:00.000Z");
    const duplicateId: Document = { ...paid };
    const documents = [paid, duplicateId];

    const result = unmarkInvoiceCollection(
      documents,
      paid.id,
      "enviado",
      "2026-06-24T11:00:00.000Z",
    );

    expect(result.documents).toBe(documents);
    expect(result.documents).toEqual([paid, duplicateId]);
    expect(findUniqueReceiptSourceInvoice(documents, paid.id)).toBeUndefined();
  });

  it("no elimina un recibo emitido si comparte ID con un borrador", () => {
    const removableReceipt = draftReceipt({ id: "receipt-duplicated" });
    const protectedReceipt = emittedReceipt({
      id: removableReceipt.id,
      number: "R-2026-0099",
    });
    const paidInvoice = paidInvoiceWithReceipt(removableReceipt);
    const documents = [paidInvoice, removableReceipt, protectedReceipt];

    const result = unmarkInvoiceCollection(
      documents,
      paidInvoice.id,
      "enviado",
      "2026-06-24T11:00:00.000Z",
      EMPTY_DATA.profile.numbering,
    );

    expect(result.documents).toBe(documents);
    expect(result.removedReceiptId).toBeUndefined();
    expect(result.documents).toContain(removableReceipt);
    expect(result.documents).toContain(protectedReceipt);
    expect(result.documents[0]).toBe(paidInvoice);
  });

  it("no desmarca si varios recibos reclaman la misma factura", () => {
    const firstReceipt = draftReceipt({ id: "receipt-first" });
    const secondReceipt = draftReceipt({ id: "receipt-second" });
    const paidInvoice = paidInvoiceWithReceipt(firstReceipt);
    const documents = [paidInvoice, firstReceipt, secondReceipt];

    const result = unmarkInvoiceCollection(
      documents,
      paidInvoice.id,
      "enviado",
      "2026-06-24T11:00:00.000Z",
      EMPTY_DATA.profile.numbering,
    );

    expect(result.documents).toBe(documents);
    expect(result.removedReceiptId).toBeUndefined();
    expect(
      findReceiptForInvoice(documents, paidInvoice.id, firstReceipt.id),
    ).toBeUndefined();
  });

  it("no desmarca un backlink huérfano o dirigido a otro tipo", () => {
    const paid = markDocumentPaid(
      { ...invoice, receiptDocumentId: "missing-receipt" },
      "2026-06-24T10:00:00.000Z",
    );
    const quote: Document = {
      ...invoice,
      id: "quote-target",
      type: "presupuesto",
      number: "P-2026-0001",
      documentSnapshot: undefined,
      pdfSnapshot: undefined,
      snapshotSeal: undefined,
      snapshotIntegrityRequired: undefined,
      status: "borrador",
    };

    const orphanDocuments = [paid];
    expect(
      unmarkInvoiceCollection(
        orphanDocuments,
        paid.id,
        "enviado",
        "2026-06-24T11:00:00.000Z",
      ).documents,
    ).toBe(orphanDocuments);

    const wrongTypeInvoice: Document = {
      ...paid,
      receiptDocumentId: quote.id,
    };
    const wrongTypeDocuments = [wrongTypeInvoice, quote];
    expect(
      unmarkInvoiceCollection(
        wrongTypeDocuments,
        wrongTypeInvoice.id,
        "enviado",
        "2026-06-24T11:00:00.000Z",
      ).documents,
    ).toBe(wrongTypeDocuments);
  });

  it("desmarcar cobro con recibo borrador puede eliminarlo sin tocar emitidos", () => {
    const removableReceipt = draftReceipt();
    const protectedReceipt = emittedReceipt({
      id: "receipt-issued",
      number: "R-2026-0002",
      sourceDocumentId: "other-invoice",
    });
    const paidInvoice = paidInvoiceWithReceipt(removableReceipt);

    const result = unmarkInvoiceCollection(
      [paidInvoice, removableReceipt, protectedReceipt],
      paidInvoice.id,
      "enviado",
      "2026-06-24T11:00:00.000Z",
      EMPTY_DATA.profile.numbering,
    );

    expect(result.removedReceiptId).toBe(removableReceipt.id);
    expect(result.documents.some((doc) => doc.id === removableReceipt.id)).toBe(
      false,
    );
    expect(result.documents.find((doc) => doc.id === protectedReceipt.id)).toBe(
      protectedReceipt,
    );
    expect(result.documents.find((doc) => doc.id === paidInvoice.id)).toMatchObject({
      status: "enviado",
      paymentStatus: "pending",
      paidAt: undefined,
      receiptDocumentId: undefined,
    });
  });

  it("no desmarca el cobro mientras exista un recibo emitido", () => {
    const receipt = emittedReceipt();
    const paidInvoice = paidInvoiceWithReceipt(receipt);
    const secondReceipt = emittedReceipt({
      id: "receipt-2",
      number: "R-2026-0003",
      sourceDocumentId: "other-invoice",
    });

    const result = unmarkInvoiceCollection(
      [paidInvoice, receipt, secondReceipt],
      paidInvoice.id,
      "enviado",
      "2026-06-24T11:00:00.000Z",
      EMPTY_DATA.profile.numbering,
    );

    expect(result.preservedReceiptId).toBe(receipt.id);
    expect(result.removedReceiptId).toBeUndefined();
    expect(result.documents.find((doc) => doc.id === receipt.id)).toBe(receipt);
    expect(result.documents.find((doc) => doc.id === secondReceipt.id)?.number).toBe(
      "R-2026-0003",
    );
    expect(result.documents.find((doc) => doc.id === paidInvoice.id)).toBe(
      paidInvoice,
    );
  });

  it("desmarcar cobro no altera snapshot/hash de factura", () => {
    const receipt = emittedReceipt();
    const paidInvoice = paidInvoiceWithReceipt(receipt);
    const snapshotHash = paidInvoice.documentSnapshot?.snapshotHash;
    const contentHash = paidInvoice.pdfSnapshot?.contentHash;

    const result = unmarkInvoiceCollection(
      [paidInvoice, receipt],
      paidInvoice.id,
      "enviado",
      "2026-06-24T11:00:00.000Z",
      EMPTY_DATA.profile.numbering,
    );
    const invoiceAfter = result.documents.find((doc) => doc.id === paidInvoice.id);

    expect(invoiceAfter?.documentSnapshot).toBe(paidInvoice.documentSnapshot);
    expect(invoiceAfter?.pdfSnapshot).toBe(paidInvoice.pdfSnapshot);
    expect(invoiceAfter?.documentSnapshot?.snapshotHash).toBe(snapshotHash);
    expect(invoiceAfter?.pdfSnapshot?.contentHash).toBe(contentHash);
  });

  it("desmarcar cobro no altera snapshot/hash de recibo emitido", () => {
    const receipt = emittedReceipt();
    const paidInvoice = paidInvoiceWithReceipt(receipt);
    const snapshotHash = receipt.documentSnapshot?.snapshotHash;
    const contentHash = receipt.pdfSnapshot?.contentHash;

    const result = unmarkInvoiceCollection(
      [paidInvoice, receipt],
      paidInvoice.id,
      "enviado",
      "2026-06-24T11:00:00.000Z",
      EMPTY_DATA.profile.numbering,
    );
    const receiptAfter = result.documents.find((doc) => doc.id === receipt.id);

    expect(receiptAfter).toBe(receipt);
    expect(receiptAfter?.documentSnapshot?.snapshotHash).toBe(snapshotHash);
    expect(receiptAfter?.pdfSnapshot?.contentHash).toBe(contentHash);
  });
});
